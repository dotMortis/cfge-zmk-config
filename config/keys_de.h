#define DE_R R
#define DE_T T
#define DE_Z Y
#define DE_U U
#define DE_I I
#define DE_O O
#define DE_P P
#define DE_A A
#define DE_S S
#define DE_D D
#define DE_F F
#define DE_G G
#define DE_H H
#define DE_J J
#define DE_K K
#define DE_L L
#define DE_Y Z
#define DE_X X
#define DE_C C
#define DE_V V
#define DE_B B
#define DE_N N
#define DE_M M

// Number and symbol keys
#define DE_1 N1
#define DE_2 N2
#define DE_3 N3
#define DE_4 N4
#define DE_5 N5
#define DE_6 N6
#define DE_7 N7
#define DE_8 N8
#define DE_9 N9
#define DE_0 N0

// Special keys
#define DE_SS MINUS  // ß on German, '-' on US
#define DE_ACCENT EQUAL // ´ on German, '=' on US

// Shifted symbol keys
#define DE_EXCLAMATION LS(N1) // !
#define DE_QUOTE LS(N2)       // "
#define DE_SECTION LS(N3)     // §
#define DE_DOLLAR LS(N4)      // $
#define DE_PERCENT LS(N5)     // %
#define DE_AMPERSAND LS(N6)   // &
#define DE_SLASH LS(N7)       // /
#define DE_LEFT_PAREN LS(N8)  // (
#define DE_RIGHT_PAREN LS(N9) // )
#define DE_EQUAL LS(N0)       // =

#define DE_QUESTION LS(MINUS) // ?
#define DE_BACKTICK LS(EQUAL) // `

// AltGr keys
#define DE_AT RA(N2)     // @
#define DE_EURO RA(E)    // €
#define DE_LEFT_BRACE RA(N7)  // {
#define DE_LEFT_BRACKET RA(N8) // [
#define DE_RIGHT_BRACKET RA(N9) // ]
#define DE_RIGHT_BRACE RA(N0)   // }
#define DE_PIPE RA(MINUS) // |
#define DE_TILDE RA(EQUAL) // ~

